<template>
  <div>
    <div 
      v-for="recipe in recipesAvailable"
      :key="recipe.title">
      <h3>{{recipe.title}}</h3>
      <ul>
        <li 
          v-for="ingredient in recipe.ingredients"
          :key="ingredient">
          {{ingredient}}
        </li>
      </ul>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'

export default {
  name: 'Recipes',
  computed: {
    ...mapGetters('chef', {
      recipesAvailable: 'recipesAvailable',
    })
  },
  created () {
    this.$store.dispatch('chef/getAvailableFood')
  }
}
</script>